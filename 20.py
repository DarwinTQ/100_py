# Busquemos si existe una letra 'Z' en la palabra "Python"
palabra = "Python"
for letra in palabra:
    if letra == 'Z':
        print("¡La letra 'Z' fue encontrada!")
        break
else:
    # Este bloque se ejecuta porque el 'for' terminó todas sus vueltas sin encontrar una 'Z' y sin un 'break'.
    print("La letra 'Z' no está en la palabra.") 

